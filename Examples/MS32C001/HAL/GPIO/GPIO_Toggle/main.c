#include "main.h"
#include "ms32c001_bsp_led.h"

/**
  * @brief  Main program.
  * @retval int
  */
int main(void)
{
  /* Reset of all peripherals, Initializes the Systick. */
  HAL_Init();

  BSP_LED_Init();

  while (1)
  {
    HAL_Delay(250);
    BSP_LED_Toggle();
  }
}

